#include <bits/stdc++.h>
using namespace std;

int main() {
    int l, r, m;
    cin >> l >> r >> m;

    long long product = 1;
    for (int i = l; i <= r; i++) {
        product = (product * i) % m;
    }

    cout << product << endl;
    return 0;
}


